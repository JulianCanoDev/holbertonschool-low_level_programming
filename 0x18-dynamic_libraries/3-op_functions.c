#include <stdio.h>
//#include <stdlib.h>

/**
 * add - add a and b number
 * @a: int variable
 * @b: int variable
 * Return: Result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substract a and b number
 * @a: int variable
 * @b: int variable
 * Return: Result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplicate a and b number
 * @a: int variable
 * @b: int variable
 * Return: Result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide a and b number
 * @a: int variable
 * @b: int variable
 * Return: Result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - module a and b number
 * @a: int variable
 * @b: int variable
 * Return: Result
 */
int mod(int a, int b)
{
	return (a % b);
}
